<x-core.modals.modal id="guide-explanation" title="What is The Guide">
    <p class="my-2">
        The <a href="/information/guide-quests" target="_blank">Guide <i class="fas fa-external-link-alt"></i></a>
        is a NPC who will, as his name implies, Guide you through the game.
    </p>
    <p class="my-2">
        <strong>It is recommended new players enable this.</strong> You get Gold and an item of your character level, max level 100,
        with enchantments that are also geared towards your level (max level 100) as well as gold for completing the requirements of each quest.
    </p>
    <p class="my-2">
        The gist of this is simple. When you register for the first time, you will be greeted by a popup,
        once and only once. Here you will see Requirements, Story and Instructions.
    </p>
    <p class="my-2">
        After which you will see a green button at the top right called: Guide Quests. You can click this to activate the
        modal to hand in or review the requirements and instructions.
    </p>
    <p class="my-2">
        After completing a Guide Quest, you can review it by clicking/tapping the hamburger menu at the top left,
        select Quest Logs and then click or tab the Completed Guide Quests.
    </p>
    <p class="my-2">
        There are instructions for both Mobile and PC players.
    </p>
    <p class="my-2">
        The Guide Quests are intended to slowly move you through various features of the game, from fighting, crafting/enchanting,
        Plane traversal and other various aspects of the game.
    </p>
    <p class="my-2">
        <strong>Once enabled you cannot disable him.</strong>
    </p>
    <p class="my-2">
        <a href="/information/settings" target="_blank">Players who reset their account</a> have the option to enable the guide quest system.
    </p>
</x-core.modals.modal>
