<x-core.modals.modal id="guide-explanation" title="What is The Guide">
    <p class="my-2">
        The <a href="/information/guide-quests" target="_blank">Guide <i class="fas fa-external-link-alt"></i></a>
        Is an NPC who, through a series of stories and <strong>explicit</strong> instructions help you through the features of the game.
    </p>
    <p class="my-2">
        The idea here is to help new players slowly get a grasp of the game. You will be rewarded with XP and currency for each quest you
        complete.
    </p>
    <p class="my-2">
        Guide Quests will take roughly 1 hour to complete, sometimes much less - such as at the begining. If a quest should take longer
        then it will explicitly state as such.
    </p>
    <p class="my-2">
        <a href="/information/settings" target="_blank">Players who reset their account</a> have the option to enable the guide quest system.
    </p>
</x-core.modals.modal>
