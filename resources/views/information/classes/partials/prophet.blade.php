<p class="mt-2">
  Focus on your Charisma and Focus stat while leveling Casting Accuracy followed
  by your class skill.
</p>
<p>
  Prophets will want either two damage spells, two healing or one of either. If
  you have two damaging spells, we add 15% of your chr to your total attack. If
  you have no healing spells equipped you will still do 30% healing of your chr
  stat.
</p>

<p>
  Prophets will always want one healing spell if they plan to get off their
  double heal special attack.
</p>
<p>
  Prophets who also wield a censer and a shield will get an extra 15% of their
  modded CHR towards their attack.
</p>
<p>
  For affixes, focus first on chr and focus based, while having at least one
  <a href="/information/enchanting">life stealing affix</a>
  and at least one
  <a href="/information/enchanting">damaging affix</a>
  .
</p>
<p>
  Prophets should focus on a good censer, shield and healing spell and play
  either Cast and Attack or Attack and Cast depending on which slot their
  healing spell and weapon is in.
</p>
<p>
  For optimal play at the beginning of your journey, use
  <a href="/information/combat">Attack and Cast</a>
  . and focus on
  <a href="/information/skill-information">Accuracy</a>
  as your healing spells will never miss. Your healing spells also have a chance
  at auto resurrecting you, the better the healing spell, the better the chance,
  two combined will stack the chance.
</p>

<p>
  Prophets will want either Dryad or High Elf as their race, to get the most of
  their class.
</p>

<p>
  You will also want to check out how
  <a href="/information/how-healing-works">healing works</a>
  within Tlessa. In a nut shell, healing spells only heal you, Prophets can
  become almost useless when all you do is heal your self. As a result, if you
  do not need to be healed, when you attack, we create a "reserve" to then be
  used - automatically - when the enemy attacks, making the combination of
  healing and damage spells beneficial. Gain a little life, take a little life.
</p>
