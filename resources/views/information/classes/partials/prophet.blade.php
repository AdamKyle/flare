<h4>Hints</h4>
<p class="mt-2">
  Focus on your Charisma and Focus stat while leveling Casting Accuracy followed by your class skill.
</p>
<p>Prophets will want either two damage spells, two healing or one of either. If you have two damaging spells we add 15% of your chr to your total attack.
Your healing will still fire as 30% of your chr stat. If you have no damaging spells, you'll cast for 2% of your chr and heal for your healing + 30% of your chr.</p>
<p>Prophets will always want one healing spell if they plan to get off their double heal special attack.</p>
<p>
  For affixes, focus first on chr and focus based, while having at least one <a href="/information/enchanting">life stealing affix</a> and at least
  one <a href="/information/enchanting">damaging affix</a>.
</p>
<p>
  Prophets do not need weapons, so if you go glass cannon, then equip two damage spells and two shields. If you are going for a safer play,
  equip a damage spell and a healing spell and two shields with additional durability enchants on them.
</p>
<p>Prophets will want either Dryad or High Elf a their race.</p>
<p>
  For Casters, start with training your <a href="/information/skills">Casting Accuracy skill</a> at 1% of your xp around level 3 or so. At which point you should
  have enough gold to purchase a good item, a good shield armour, a ring or two or an artifact or two. Once casting accuracy has reached a few levels attempt to switch over
  to casting.
</p>
<p>
  Casters and Vampires are the hardest class to play from the start because their attacks are tied to their stats more closely then other classes. This can make early
  game a bit of a grind to get going, but once you do it will pay off in the long run as Heretics and Vampires are some of the end games strongest classes.
  For your cast to work properly, unless its a heal spell, you need a Casting Accuracy of at least level 5 to hit more then miss.
</p>
<p>For prophets, use a weapon and a shield with a healing spell and damage spell and a ring or two as well as body armour to start for higher stat increase.</p>
<p>Follow the same concept for casters to level until you start training the Casting Accuracy. You can then choose to cast or cast and attack.</p>