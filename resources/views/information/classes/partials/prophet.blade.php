<h2 class="tw-font-light">Hints</h2>
<hr />
<p class="mt-2">
  Focus on your Charisma and Focus stat while leveling Casting Accuracy followed by your class skill.
</p>
<p>Prophets will want either two damage spells, two healing or one of either. If you have two damaging spells we add 15% of your chr to your total attack.
If you have no healing spells equipped you will still do 30% healing of your chr stat, either modded (not voided) or regular (voided).</p>

<p>Prophets will always want one healing spell if they plan to get off their double heal special attack.</p>
<p>
  For affixes, focus first on chr and focus based, while having at least one <a href="/information/enchanting">life stealing affix</a> and at least
  one <a href="/information/enchanting">damaging affix</a>.
</p>
<p>
  Prophets should focus on a good weapon, shield and healing spell and play either Cast and Attack or Attack and Cast
  depending on which slow their healing spell and weapon is in.
</p>
<x-core.alerts.info-alert>
  <p>
    When starting out, in your low levels, you will want a weapon, a shield and a healing spell. From there, you will want to either Attack and Cast
    or Cast and attack, depending on which hand your weapon/shield and slot healing spell is equipped in.
  </p>
</x-core.alerts.info-alert>
<p>Prophets will want either Dryad or High Elf a their race.</p>
<p>
  For Casters, start with training your <a href="/information/skill-information">Casting Accuracy skill</a> at 10% of your xp around level 3 or so.
</p>