<p class="mt-2">Focus on Durability followed by Accuracy and Criticality.</p>
<p>It is suggested you equip claws or two shields.</p>
<p>
    Load yourself up with
    <a href="/information/enchanting">life stealing affixes</a>
    and
    <a href="/information/enchanting">durability affixes</a>
    , along with a
    <a href="/information/enchanting">Damage based affix</a>
</p>
<p>
    There are two attack types you could do. One that involves purely attacking
    or one that involves purely casting. For vampires it is recommended you go
    with attacking. The reason for this, is because your life stealing affixes
    will not do enough damage to kill the enemy and, if the damage you do via
    attacking is strong enough your life stealing affixes will not fire.
</p>
<p>
    The issue with cast, is you do not do enough damage as a pure "caster vamp".
</p>
<p>
    Vampire's life stealing affixes will scale with enemy health. The idea is
    that you want to fight the hardest thing you can kill in one hit that
    triggers your enchantments to fire. You keep killing that creature until
    your enchantments no longer fire, and then move on. This allows vampires to
    take on creatures that are a bit tougher than them.
</p>
