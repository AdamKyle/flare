<p class="mb-4">
    Beastmasters are hunters who have learned to channel the fury of beasts into devastating attacks.
    They bridge the gap between raw power and calculated precision, using bows to pierce from a distance
    and hammers to crush what stands before them.
</p>
<p class="mb-4">
    Beastmasters have two weapon-based specials depending on what is equipped.
</p>
<p class="mb-4">
    With a bow equipped, Beastmasters loose a Devil's Piercing Shot that deals double bow damage followed by
    four bleeds at 17%, 14%, 8%, and 4% of the enemy's current health.
</p>
<p class="mb-4">
    With a hammer equipped, Beastmasters unleash a Beast Stomp that deals double hammer damage and then
    shatters the earth crust beneath the enemy for 25% of its current health.
</p>
