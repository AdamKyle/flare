<p class="mt-2">
    Dancers are those who use props, like fans, to entice and seduce their enemy while slicing them to bits and laughing about it afterward.
</p>
<p>
    Agility is how you hit while Dexterity is how you deal damage. Dancers like <a href='/information/combat'>Attack</a> when it comes to damage.
</p>
<p>
    Dancers have a special called: <strong>Sensual Dance</strong>.
</p>
<p>
    If you have one Fan equipped, you will dance around the enemy dealing 30% of your weapon damage 3 times.
</p>
<p>
    If you have two Fans equipped, you will dance with aggression and deal 15% of your weapon damage 9 times.
</p>
<p>
    Your special can proc if you have leveled your <a href='/information/class-skils'>Class Skill</a> and have a high enough skill bonus.
</p>
