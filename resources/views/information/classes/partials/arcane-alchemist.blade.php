
<p class="mt-2">
    Arcane Alchemists prefer to use staves above other weapons as it gives them an increase to their damage.
    Arcane Alchemists are also as proficient with damaging spells like Heretics and are ok at using Healing spells.
</p>
<p>
    Charisma is how you hit enemies, in conjunction with Accuracy or Casting Accuracy or both.
</p>
<ul>
    <li>With a stave equipped you do +15% modded chr on top of your damage.</li>
    <li>If you have a healing spell equipped you will do 10% of your modded chr to healing and 10% to damage (this will add to the 15% from the stave)</li>
    <li>Like Heretics, if you have a damage spell equipped you do 30% of your modded int as damage on top of the spell damage + bonuses.</li>
</ul>
<p>
    If you cannot tell by now, <strong>Your starting gear should be 1 stave, 1 healing spell and 1 damage spell and 1 body for armour</strong>.
</p>
<p>
    This class can become overpowered quickly and can become rather powerful early game if you buy a stave, damage spell and healing spell.
</p>
<p>
    Arcane Alchemists have a special attack called: <strong>The Alchemists Ravenous Dream</strong>.
</p>
<p>
    This does between 2 and 6 explosions. The first is 10% of your int, and every attack after that would add 3% to the 10%.
    For example if you hit 3 times that's: 10% of your int, 13% of your int and finally 16% of your int.
</p>
<p>
    <strong>Crafting Skill Bonus:</strong> Arcane Alchemists get 15% bonus towards XP and Skill Bonus when crafting Alchemy Items and Spell Crafting
</p>
