<p class="mt-2">
  Focus on raising your Agility and Dexterity. Train your Accuracy skill to make sure you can hit something, then your class skill.
</p>
<p>It is suggested you only equip a bow as your class skill has a chance to fire the damaging aspect and do triple attacks.</p>
<p>
  For affixes, focus first on dexterity and agility based, while having at least one <a href="/information/enchanting">life stealing affix</a> and at least
  one <a href="/information/enchanting">damaging affix</a>.
</p>
<p>
  Rangers who use Cast and Attack or Attack and cast can heal with out spells, at 15% of their CHR. If a ranger has a healing spell equipped they still get the
  additional 15% chr added to their healing output.
</p>
<p>Rangers will want a race that quick and nimble (High Elf or Centaur) or focuses on dexterity, Orc.</p>