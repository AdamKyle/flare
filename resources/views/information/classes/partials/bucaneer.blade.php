<p class="mb-4">
    Buccaneers fight from behind battered shields and old-world guns, mixing defensive grit with brutal close-range gunfire.
</p>

<p class="mb-4">
    Buccaneers prefer guns and shields. Two separate specials are available depending on what is equipped.
</p>

<p class="mb-4">
    With a gun and shield equipped, Buccaneers fire a three-shot barrage dealing 25%, 15%, and 5% of character damage per shot.
</p>

<p class="mb-4">
    With exactly two guns equipped, Buccaneers fire a stronger three-shot barrage dealing 75%, 55%, and 35% of character damage per shot.
</p>
