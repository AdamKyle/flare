<h4>Hints</h4>
<p class="mt-2">
  Focus on raising your Strength and Dexterity. Train your Accuracy skill to make sure you can hit something, then based on your
  preferred attack style (duel wield or single with shield) level your class skill to get the added benefits.
</p>
<p>It is not suggested you equip a bow as your class skill will give you no bonus.</p>
<p>Equipping a bow also will prevent your class skill fro having a chance to fire the special double attack.</p>
<p>
  For affixes, focus first on str and dex based, while having at least one <a href="/information/enchanting">life stealing affix</a> and at least
  one <a href="/information/enchanting">damaging affix</a>.
</p>
<p>
  Fighters should use either attack or defend (15% of your str is added to your over all ac) but can use attack and cast where their spell would be a healing spell.
  If you go this route, equip a shield for added durability (health).
</p>
<p>If you go the weapon/shield route, make sure to add some extra dur (through enchanting) to that shield and level your class skill for the extra armour class bonus.</p>
<p>Fighters will want a race that provides good strength and dex: Orc (dex and str - great for duel wielding), Dark Dwarf/Centaur (str and dur - great for weapon/shield).</p>