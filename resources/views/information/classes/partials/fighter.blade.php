<p class="mt-2">
    Fighters core stats are Strength for Damage and Dexterity to hit anything.
    Fighters should equip one weapon and one shield or two weapons and use the
    attack button. (See
    <a href="/information/combat">Combat</a>
    docs).
</p>
<p>
    When attacking with no swords, Fighters will do 5% of their strength as
    their attack, while other classes do 2% of their respective attacking stat.
    When attacking with swords you use 15% of your strength towards the overall
    damage.
</p>
<p>
    When using a shield, you will use your class bonus, which can be raised via
    <a href="/information/enchanting">Enchantments</a>
    towards your defence, in conjunction with all other bonuses.
</p>
<p>
    Fighters will want a race that provides good strength and dex: Orc (dex and
    str - great for dual wielding), Dark Dwarf/Centaur (str and dur - great for
    weapon/shield).
</p>
