<h4>Hints</h4>
<p class="mt-2">
  Focus on raising your Agility and Dexterity. Train your Accuracy skill to make sure you can hit something, then your class skill.
</p>
<p>Thieves will want to duel wield if they want as their class skill to fire before they attack to see if they can "dance through the shadows" and auto hit.</p>
<p>
  For affixes, focus first on dexterity and agility based, while having at least one <a href="/information/enchanting">life stealing affix</a> and at least
  one <a href="/information/enchanting">damaging affix</a>.
</p>
<p>
  Thieves should use attack as they are duel wielding over anything else.
</p>
<p>Thieves will want a race that quick and nimble (High Elf or Centaur) or focuses on dexterity, Orc.</p>