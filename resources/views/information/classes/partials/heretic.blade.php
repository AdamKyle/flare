<h2 class="font-light">Hints</h2>
<hr />
<p class="mt-2">
  Focus on your Intelligence and Focus stat while leveling Casting Accuracy followed by your class skill.
</p>
<p>Heretics will want two shields, added dur, and two damage spells. This will allow their class skill to fire off the double cast chance.</p>
<x-core.alerts.info-alert>
  <p>
    When you are first starting out, it is best to have a shield and a weapon, till you can hit something, make some XP and then start focusing on Casting
    Accuracy.
  </p>
</x-core.alerts.info-alert>
<p>Heretics also get 3% of their intelligence added to their spell damage or 2% if they have no damage spells equipped.</p>
<p>
  For affixes, focus first on int and focus based, while having at least one <a href="/information/enchanting">life stealing affix</a> and at least
  one <a href="/information/enchanting">damaging affix</a>.
</p>
<p>Heretics can cast with out spells at 2% of their intelligence mod. This includes: Cast, Cast and Attack and Attack and Cast</p>
<p>Heretics are glass cannon and should only focus on cast with no healing spells. They do not get any healing bonuses either.</p>
<p>Heretics will want either Dryad or High Elf a their race.</p>
<h4>Tips for Early Game</h4>
<p class="mt-4">
  When you start you might be tempted to buy some damage spells and equip them and start casting. But you will miss a lot, until you start leveling Casting Accuracy.
</p>
<p>
  For Heretics, start with training your <a href="/information/skills">Casting Accuracy skill</a> at 10% of your xp around level 3 or so.
  For your cast to work properly, unless its a heal spell, you need a Casting Accuracy of at least level 5 to hit more then miss.
</p>
<p>
  Heretics are easier then prophets, as with nothing equipped they can still do 2% of their int as damage, unless you use Defend.
  However, both classes will suffer at early levels until you are able to get the Casting Accuracy up.
</p>