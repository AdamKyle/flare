<h4>Hints</h4>
<p class="mt-2">
  Focus on your Intelligence and Focus stat while leveling Casting Accuracy followed by your class skill.
</p>
<p>Heretics will want two shields, added dur, and two damage spells. This will allow their class skill to fire off the double cast chance./p>
<p>Heretics also get 30% of their intelligence added to their spell damage.</p>
<p>
  For affixes, focus first on int and focus based, while having at least one <a href="/information/enchanting">life stealing affix</a> and at least
  one <a href="/information/enchanting">damaging affix</a>.
</p>
<p>Heretics can cast with out spells at 2% of their intelligence mod.</p>
<p>Heretics are glass cannon and should only focus on cast with no healing spells. They do not get any healing bonuses either.</p>
<p>Prophets will want either Dryad or High Elf a their race.</p>
<h4>Tips for Early Game</h4>
<p class="mt-4">
  When you start you might be tempted to buy some damage spells and equip them and start casting. But you will miss a lot, until you start leveling Casting Accuracy.
</p>
<p>
  For Casters, start with training your <a href="/information/skills">Casting Accuracy skill</a> at 1% of your xp around level 3 or so. At which point you should
  have enough gold to purchase a good item, a good shield armour, a ring or two or an artifact or two. Once casting accuracy has reached a few levels attempt to switch over
  to casting.
  For your cast to work properly, unless its a heal spell, you need a Casting Accuracy of at least level 5 to hit more then miss.
</p>
<p>
  Casters and Vampires are the hardest class to play from the start because their attacks are tied to their stats more closely then other classes. This can make early
  game a bit of a grind to get going, but once you do it will pay off in the long run as Heretics and Vampires are some of the end games strongest classes.
</p>