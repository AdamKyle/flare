<p class="mb-4">
    Alcoholics are a mix between Blacksmith (heavy drinkers) and Fighters (also heavy drinkers).
</p>
<p class="mb-4">
    Alcoholics suffer a 25% damage reduction when using any form of weapon and a 50% reduction when using spells (damage or healing).
    The reason: You are far too drunk half the time to properly use these.
</p>
<p class="mb-4">
    Alcoholics suffer no damage deduction when using fists at 25% of the core damage stat.
</p>
<p class="mb-4">
    Alcoholics <a href="/information/class-ranks">class specials</a> focus mostly on raising your health and damage modifier
    as well as your damage stat.
</p>
<p class="mb-4">
    Alcoholics have two special attacks that can proc depending on your class bonus %. One is Deathly puke, which has a very small chance to auto kill the enemy.
    Otherwise, it will do 15% of your damage stat.
</p>
<p class="mb-4">
    The other special attack is Bloody puke: Deal 30% of your health as damage, but also take 30% of your health in damage.
</p>
