<p class="mt-2">
    Gunslingers are characters who prefer to use guns as their primary weapons. These guns are fashioned from the
    "Old World" guns which have been modified and changed to resemble more of a steampunk based design then a old world modern gun.
</p>
<p>
    Dexterity is how you hit your enemy with agility being the core aspect of your damage. Gunslingers like (<a href='/information/combat'>Attack</a> when it comes to damage).
</p>
<p>
    Gunslingers have a special called: <strong>Gunslingers Assassination</strong>.
</p>
<p>
    If you have one gun equipped, you will deal 60% of your weapon damage as a snipe based attack.
</p>
<p>
    If you have two guns equipped, you will deal 40% of your weapon damage six times.
</p>
<p>
    Your special can proc if you have leveled your <a href='/information/class-skils'>Class Skill</a> and have a high enough skill bonus.
</p>
