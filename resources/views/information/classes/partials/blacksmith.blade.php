<p class="mt-2">
    Blacksmiths are a class that uses hammers, a new weapon type, which - much like bows - are two-handed weapons. As a result, hammers give slightly more Strength
    than other weapons.
</p>
<p>
    Dexterity is how you hit enemies, and this class gives no bonuses to your dexterity nor does the weapon give bonuses.
    As a result new players should buy good Body armour and then focus on enchantments that Raise Dexterity.
</p>
<p>
    As long as you have a hammer equipped you have a % chance to do your special attack Hammer Smash.
</p>
<p>
    The way Hammer Smash works is as follows:
</p>
<ul>
    <li>First we take 30% of your modded Strength as damage to the enemy</li>
    <li>Next you have a 60% on a 1/100 chance to do what's called After Shocks (you will do 3 if your roll is higher than 99).</li>
    <li>Each After Shock takes the 30% of modded Strength and reduces the damage by 15% for each After Shock.</li>
</ul>
<p>
    In the above list, let's give an example of an aftershock: Let's assume you're 30% of STR was 1,000,000.
    Your first aftershock would be 15% of 1,000,000,000 = 850000, your second is 850000 - 15% = 722500, your final would be:
    722500 - 15% = 614125
</p>
<p>
    <strong>Crafting Skill Bonus:</strong> Black Smiths get 15% bonus towards XP and Skill Bonus when crafting Weapons, Armour and Rings.
</p>
